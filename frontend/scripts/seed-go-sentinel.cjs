#!/usr/bin/env node
/*
 * seed-go-sentinel.js — drop a stub frontend/node_modules/go.mod so
 * Go's package walker stops at the node_modules boundary instead of
 * recursing into frontend/node_modules/flatted/golang/pkg/flatted/,
 * which ships a stray .go file that would otherwise become part of
 * the recall Go module.
 *
 * Runs as a postinstall hook from package.json. The sentinel file
 * has to live INSIDE node_modules so that:
 *
 *   1. Every `npm ci` (which deletes and recreates node_modules)
 *      re-creates the sentinel automatically — no manual reseed.
 *   2. `//go:embed all:frontend/dist` in assets.go still works,
 *      because frontend/dist remains inside the recall module
 *      (the sentinel only carves out node_modules).
 *
 * See TECHNICAL_DEBT.md #5 for the history of why this is needed
 * (the `flatted` npm package's golang/ subdirectory) and the
 * alternatives that were ruled out (filtering scripts, moving the
 * Go module root).
 */

'use strict'

const fs = require('node:fs')
const path = require('node:path')

const target = path.join(__dirname, '..', 'node_modules', 'go.mod')
const body = `// Sentinel sub-module: makes \`go list ./...\` from the repo root
// stop at this boundary so the recall module no longer absorbs the
// stray .go file inside frontend/node_modules/flatted/golang/.
//
// AUTO-GENERATED by frontend/scripts/seed-go-sentinel.js, which
// runs as an npm postinstall hook so every \`npm ci\` recreates
// the file. Do not commit it; do not depend on it from any Go
// code. See TECHNICAL_DEBT.md #5 for the rationale.

module recall-frontend-node-modules-boundary

go 1.26
`

try {
  // node_modules exists by the time postinstall fires; defensively
  // mkdir -p in case a future tool deletes it between phases.
  fs.mkdirSync(path.dirname(target), { recursive: true })
  fs.writeFileSync(target, body)
  // No log line on success — keep `npm ci` output tidy.
} catch (err) {
  // Non-fatal: if seeding fails, downstream tools will still work
  // (with the historical filter workarounds). Print a warning so a
  // contributor with a strange permission setup notices.
  process.stderr.write(`seed-go-sentinel: warning: ${err.message}\n`)
}
